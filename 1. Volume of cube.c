#include <stdio.h>

int main() {
    // Declare variables
    int height = 10; // in cm
    int width = 12;  // in cm
    int depth = 8;   // in cm

    // Calculate volume
    int volume = height * width * depth;

    // Display the result
    printf("Volume of the cube (box) is: %d cubic cm\n", volume);

    return 0;
}
